public class PrimitiveOperations {
	public static void main(String[] args) {
		int intVar = 7;
		double dubVar = 2.701;
		System.out.println(intVar);
		System.out.println(dubVar);

		double productVar = dubVar * intVar;
		System.out.println(productVar);

		float fIntVar = intVar;
		System.out.println(fIntVar);

		int iDubVar = (int)dubVar;
		System.out.println(iDubVar);

		char charVar = 'H';
		System.out.println(charVar);

		char lowerCharVar = (char)(charVar + 32);
		System.out.println(lowerCharVar);
		
		
	}
}


